
package basicjava1;

public class IncrementDecrement {
    public static void main(String[] args){
        
        int x = 25;
        int y;
        
        y = --x;//24
        System.out.println("y = "+y);
        
        y = x--;//24
        System.out.println("y = "+y);
        
        y = ++x;//24
        System.out.println("y = "+y);
        
        y = x++;//24
        System.out.println("y = "+y);
        
        y = --x;//24
        System.out.println("y = "+y);
        
        y = --x;//23
        System.out.println("y = "+y);
        
    }
}
        
     