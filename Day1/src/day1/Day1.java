
 
package day1;


public class Day1 {

   
    public static void main(String[] args) {
        int i,x,y;
        x = 1;
        y = 10;
        for(i=x;i<=y;i++){
            if(i%2 == 0){
                System.out.println("even:"+i);
            }
            else{
                System.out.println("odd:"+i);
            }
        }
     
       
    }
    
}
